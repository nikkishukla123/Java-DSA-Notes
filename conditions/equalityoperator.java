package conditions;

public class equalityoperator {
    public static void main(String[] args) {
        int a = 5;
        int b = 6;
        System.out.println(a==b); //equality operator
        System.out.println(a!=b); // relational
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a>b);
        System.out.println(a<b);
    }
}
