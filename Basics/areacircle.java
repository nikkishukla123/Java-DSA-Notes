package Basics;

public class areacircle {
    public static void main(String[] args) {
        double r = 6.5;
        double a = Math.PI * r *r;
        System.out.println("the area of circle is:" + a);

    }
}
// The above code snippet calculates the area of a circle with a radius of 6.5. It uses the formula for the area of a circle, which is πr^2, where r is the radius of the circle. It then prints the calculated area. The Math.PI constant is used to represent the value of π in Java.
